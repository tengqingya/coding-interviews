    /**
     * 不使用额外节点
     * @param head1
     * @param head2
     * @return
     */
    public static ListNode merge3(ListNode head1, ListNode head2) {
        ListNode newHead = head1;
        ListNode l1 = head1.next;
        ListNode l2 = head2.next;
        int value1=0;
        int value2=0;
        int value3=0;
        int value4=0;
        while( l1!=null){
            value1 = head1.value;
            value2 = l1.value;
            value3 = head2.value;
            value4 = l2.value;
            if(value1<=value3 && value3<=value2){
                head1.next = head2;
                head2.next=l1;
                head2 = l2;
                l2=l2.next;
                head1=l1;
                l1=l1.next;
            }else if(value1<=value3 ){
                head1=l1;
                l1=l1.next;
            }else if(value4>value1){
                head2.next=head1;
                head2 = l2;
                l2=l2.next;
            }
        }
        if(value1<=value3){
            head1.next = head2;
        }
        return newHead;
    }


    /**
     * 输入两个递增排序的链表，合并这两个链表并使新链表中的结点仍然是按照递增排序的
     *
     * @param head1 第一个有序链表
     * @param head2 第二个有序链表
     * @return 合并后的有序链表头
     */


    /**
     * 使用2个额外节点
     * @param head1
     * @param head2
     * @return
     */
    public static ListNode merge(ListNode head1, ListNode head2) {
        // 如果第一个链表为空，返回第二个链表头结点
        if (head1 == null) {
            return head2;
        }

        // 如果第二个结点为空，返回第一个链表头结点
        if (head2 == null) {
            return head1;
        }

        // 创建一个临时结点，用于添加元素时方便
        ListNode root = new ListNode();
        // 用于指向合并后的新链的尾结点
        ListNode pointer = root;

        // 当两个链表都不为空就进行合并操作
        while (head1 != null && head2 != null) {
            // 下面的操作合并较小的元素
            if (head1.value < head2.value) {
                pointer.next = head1;
                head1 = head1.next;
            } else {
                pointer.next = head2;
                head2 = head2.next;
            }

            // 将指针移动到合并后的链表的末尾
            pointer = pointer.next;
        }

        // 下面的两个if有且只一个if会内的内容会执行

        // 如果第一个链表的元素未处理完将其，接到合并链表的最后一个结点之后
        if (head1 != null) {
            pointer.next = head1;
        }

        // 如果第二个链表的元素未处理完将其，接到合并链表的最后一个结点之后
        if (head2 != null) {
            pointer.next = head2;
        }

        // 返回处理结果
        return root.next;
    }


    /**
     * 输入两个递增排序的链表，合并这两个链表并使新链表中的结点仍然是按照递增排序的
     * 【使用的是递归的解法，不推荐，递归调用的时候会有方法入栈，需要更多的内存】
     *
     * @param head1 第一个有序链表
     * @param head2 第二个有序链表
     * @return 合并后的有序链表头
     */
    public static ListNode merge2(ListNode head1, ListNode head2) {
        // 如果第一个链表为空，返回第二个链表头结点
        if (head1 == null) {
            return head2;
        }

        // 如果第二个链表为空，返回第一个链表头结点
        if (head2 == null) {
            return head1;
        }

        // 记录两个链表中头部较小的结点
        ListNode tmp = head1;
        if (tmp.value < head2.value) {
            // 如果第一个链表的头结点小，就递归处理第一个链表的下一个结点和第二个链表的头结点
            head1.next = merge2(head1.next, head2);
        } else {
            // 如果第二个链表的头结点小，就递归处理第一个链表的头结点和第二个链表的头结点的下一个结点
            tmp = head2;
            head2.next = merge2(head1, head2.next);
            out.println(tmp.next.value);
            out.println(head2.next.value);
        }

        // 返回处理结果
        return tmp;
    }

    /**
     * 输出链表的元素值
     *
     * @param head 链表的头结点
     */
    public static void printList(ListNode head) {
        while (head != null) {
            out.print(head.value + "->");
            head = head.next;
        }
        out.println("null");
    }

